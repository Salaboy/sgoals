package com.salaboy.sgoals.model.impl;

import com.salaboy.sgoals.model.api.Atom;
import java.util.Objects;

/**
 *
 * @author salaboy
 */
public abstract class AbstractAtom implements Atom{
    private String name;

    public AbstractAtom(String name) {
        this.name = name;
    }

    @Override
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 31 * hash + Objects.hashCode(this.name);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final AbstractAtom other = (AbstractAtom) obj;
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return name;
    }
    
    
}
